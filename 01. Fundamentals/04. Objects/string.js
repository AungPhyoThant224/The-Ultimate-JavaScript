let message = "This is my first message.";

/**
 * Return the length of String
 */
console.log(message.length);

/**
 * Check 'first' is included or not
 */
console.log(message.includes("first"));

/**
 * Return the index of 'is'
 */
console.log(message.indexOf("is"));

/**
 * Replace the first with second in the String
 */
console.log(message.replace("first", "second"));

/**
 * Remove the white spaces
 */
console.log(message.trim());

/**
 * Split the String form ' ' and return an array
 */
console.log(message.split(" "));

